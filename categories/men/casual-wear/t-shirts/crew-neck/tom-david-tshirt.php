            if (quantity > 1) {
                quantity--;
                quantityDisplay.textContent = quantity;
            }
        });
        // Increase quantity
        document.getElementById('increaseBtn').addEventListener('click', function() {
            if (quantity < 10) {
                quantity++;
                quantityDisplay.textContent = quantity;
